    def CapturarTeclas(self,event):
        if self.BanderaEjecucion:
            if event.text()=='e' or event.text()=='E':
                if not self.Bandera_P_C:
                    self.Bandera_Error = True
            elif event.text()=='p' or event.text()=='P':
                self.Bandera_P_C = True
                self.stackedWidget.setCurrentWidget(self.page_Procesos)
            elif event.text()=='i' or event.text()=='I':
                if not self.Bandera_P_C:
                    self.BanderaInterrupcion = True
        if self.isAlgoritmoCorriendo:
            if event.text()=='n' or event.text()=='N':
                Logica.FabricarNuevoMiniom(self,len(self.Lotes)+1)
                Logica.DarDeAltaBecario(self)
            elif event.text()=='c' or event.text()=='C':
                self.Bandera_P_C = False
                self.stackedWidget.setCurrentWidget(self.page_Procesos)
            elif event.text()=='t' or event.text()=='T':
                self.Bandera_P_C = True
                Logica.MostrarPestañaTiempos(self)
            elif event.text()=='a' or event.text()=='A':
                self.Bandera_P_C = True
                Logica.MostrarPestañaPaginacion(self)
            elif event.text()=='s' or event.text()=='S':
                if self.tableWidget_2.rowCount() != 0:
                    Logica.PalGabacho(self)
            elif event.text()=='r' or event.text()=='R':
                if len(self.Suspendidos) != 0:
                    Logica.DeportarMexas(self)
